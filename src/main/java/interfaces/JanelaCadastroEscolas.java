    private void inserirEscola(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirEscola
        Escola escola = obterEscolaInformada();
        String mensagem_erro = null;
        if(escola != null) mensagem_erro = controlador.inserirEscola(escola);
        else mensagem_erro = "Algum atributo da escola nao foi encontrada";
                if (mensagem_erro == null){
                    Escola visão = escola.getVisão();
                    escolascadastradasComboBox.setSelectedItem(visão);
                    escolascadastradasComboBox.addItem(visão);
                }else informarErro (mensagem_erro);
    }//GEN-LAST:event_inserirEscola

    private void consultarEscola(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarEscola
        Escola visão = (Escola) escolascadastradasComboBox.getSelectedItem ();
        Escola escola = null;
        String mensagem_erro = null;
        if (visão != null) {
            escola = Escola.buscarEscola (visão.getCnpj());
            if (escola == null) mensagem_erro = "Escola não cadastrada";
        } else mensagem_erro = "Nenhuma escola selecionada";
        if (mensagem_erro == null) {
            cnpjTextField.setText(escola.getCnpj());
            String nome = escola.getNome();
            if (nome == null) nome = "";
            nomeTextField.setText(nome);
            bibliotecaCheckBox.setSelected(escola.isBiblioteca());
            nalunosTextField.setText(Integer.toString(escola.getAlunosMatriculados()));
            selecionarTipoRadioButton(escola.getTipo().ordinal());
        } else informarErro (mensagem_erro);

    }//GEN-LAST:event_consultarEscola

    private void removerEscola(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerEscola

        Escola visão = (Escola) escolascadastradasComboBox.getSelectedItem ();
        String mensagem_erro = null;
        if (visão != null) mensagem_erro = controlador.removerEscola(visão.getCnpj());
        else mensagem_erro = "Nenhum escola selecionado";
        if (mensagem_erro == null) {
            escolascadastradasComboBox.removeItem(visão);
            limparcampos(evt);
        } else informarErro (mensagem_erro);

    }//GEN-LAST:event_removerEscola

    private void limparcampos(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_limparcampos
        nomeTextField.setText("");
        cnpjTextField.setText("");
        nalunosTextField.setText("");
        tipoButtonGroup.clearSelection();
        bibliotecaCheckBox.setSelected(false);
    }//GEN-LAST:event_limparcampos

    private void alterarEscola(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarEscola
        Escola escola = obterEscolaInformada();
        String mensagem_erro = null;
        if (escola != null) mensagem_erro = controlador.alterarEscola(escola);
        else mensagem_erro = "Algum atributo da escola não foi informado";
        if (mensagem_erro == null) {
        Escola visão = getVisãoAlterada(escola.getCnpj());
        if (visão != null) {
            visão.setNome(escola.getNome());
            escolascadastradasComboBox.setSelectedItem(visão);
            escolascadastradasComboBox.updateUI();
        }
        } else informarErro (mensagem_erro);
    }//GEN-LAST:event_alterarEscola
    private Escola getVisãoAlterada(String cnpj) {
        for (Escola visão : escolas_cadastradas) {
            if (visão.getCnpj().equals(cnpj)) return visão;
        }
        return null;
    }
