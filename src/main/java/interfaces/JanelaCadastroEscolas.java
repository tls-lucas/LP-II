    private Escola obterEscolaInformada() {
        String cnpj = cnpjTextField.getText();
        if (cnpj.isEmpty()) return null;
        String nome = nomeTextField.getText();
        if (nome.isEmpty()) return null;
        String nalunos_str = nalunosTextField.getText();
        boolean biblioteca = bibliotecaCheckBox.isSelected();
        if (nalunos_str.isEmpty()) return null;
        int nalunos = Integer.parseInt(nalunosTextField.getText());
        Tipo tipo = null;
        if(tipoButtonGroup.getSelection() != null)
            tipo = Tipo.values()[tipoButtonGroup.getSelection().getMnemonic()];
        else return null;
        return new Escola(cnpj, nome, nalunos, tipo, biblioteca);
    }
    
    
    private void informarErro (String mensagem) {
        JOptionPane.showMessageDialog (this, mensagem, "Erro", JOptionPane.ERROR_MESSAGE);
    }
    
    private void selecionarTipoRadioButton(int índice_tipo) {
        switch(índice_tipo) {
            case 0: publicaRadioButton.setSelected(true);
            break;
            case 1: particularRadioButton.setSelected(true);
            break;
        }
    }
